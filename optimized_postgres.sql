/*
    Полная PostgreSQL-версия исходного скрипта MS SQL Server.

    Что сохранено:
    - все основные таблицы из исходного скрипта;
    - индексы;
    - значения по умолчанию через ALTER TABLE;
    - внешние ключи через ALTER TABLE;
    - все функции присутствуют.

    Что изменено:
    - синтаксис MS SQL Server переведен на синтаксис PostgreSQL;
    - bit -> boolean;
    - datetime -> timestamp;
    - image -> bytea;
    - identity(1,1) -> generated by default as identity;
    - SYSTEM_USER / suser_sname() -> current_user;
    - dbo.F_WORKITEMS_COUNT_BY_ID_WORK(id_work, is_complit) оптимизирована:
      теперь она принимает массив идентификаторов заказов и возвращает счетчики
      для всех этих заказов одним наборным запросом с GROUP BY;
    - F_WORKS_LIST использует эту функцию один раз, а не два раза на каждую строку.

    Проблемный фрагмент в исходном MS SQL Server-скрипте:

        dbo.F_WORKITEMS_COUNT_BY_ID_WORK(works.Id_Work,0) as WorkItemsNotComplit,
        dbo.F_WORKITEMS_COUNT_BY_ID_WORK(works.Id_Work,1) as WorkItemsComplit,

    Оптимизированный вариант в PostgreSQL:

        work_ids as (
            select array_agg(tw.id_work) as ids
            from top_works tw
        ),
        workitem_counts as (
            select c.*
            from work_ids ids
            cross join f_workitems_count_by_id_work(ids.ids) c
        )

    Количество вызовов функции меняется с:

        3000 * 2 = 6000 вызовов

    на:

        1 вызов
*/

