-- ------------------------------------------------------------
-- Исходная идея из SQL Server:
--
-- F_EMPLOYEE_GET()
-- возвращает идентификатор текущего сотрудника,
-- сравнивая Employee.Login_Name с SYSTEM_USER.
--
-- Версия для PostgreSQL:
-- SYSTEM_USER / suser_sname() -> current_user.
-- ------------------------------------------------------------

create or replace function f_employee_get()
returns integer
language sql
stable
as $$
    select e.id_employee
    from employee e
    where e.login_name = current_user
    limit 1;
$$;

-- ------------------------------------------------------------
-- Исходная идея из SQL Server:
--
-- F_EMPLOYEE_FULLNAME(@ID_EMPLOYEE)
-- если id равен null, использовать F_EMPLOYEE_GET();
-- если id = -1, вернуть пустую строку;
-- иначе вернуть строку вида 'Фамилия И. О.';
-- если результат пустой, вернуть Login_Name.
-- ------------------------------------------------------------

create or replace function f_employee_fullname(p_id_employee integer)
returns varchar(101)
language sql
stable
as $$
    with employee_id as (
        select coalesce(p_id_employee, f_employee_get()) as id_employee
    )
    select
        cast(
            case
                when employee_id.id_employee = -1 then ''
                else
                    coalesce(
                        nullif(
                            rtrim(
                                replace(
                                    e.surname || ' ' ||
                                    upper(substr(e.name, 1, 1)) || '. ' ||
                                    upper(substr(e.patronymic, 1, 1)) || '.',
                                    '. .',
                                    ''
                                )
                            ),
                            ''
                        ),
                        e.login_name,
                        ''
                    )
            end
        as varchar(101))
    from employee_id
    left join employee e
        on e.id_employee = employee_id.id_employee;
$$;

-- ------------------------------------------------------------
-- Исходная функция SQL Server:
--
-- F_WORKITEMS_COUNT_BY_ID_WORK(@id_work, @is_complit)
-- возвращала одно число для одного заказа и одного признака завершенности.
--
-- Проблема:
-- F_WORKS_LIST вызывала ее два раза на каждую строку:
--
--     F_WORKITEMS_COUNT_BY_ID_WORK(works.Id_Work, 0)
--     F_WORKITEMS_COUNT_BY_ID_WORK(works.Id_Work, 1)
--
-- Для 3000 заказов это 6000 вызовов функции.
--
-- Оптимизированная версия для PostgreSQL:
-- один вызов принимает integer[] и возвращает оба счетчика для всех заказов.
-- ------------------------------------------------------------

create or replace function f_workitems_count_by_id_work(
    p_work_ids integer[]
)
returns table
(
    id_work integer,
    workitemsnotcomplit integer,
    workitemscomplit integer
)
language sql
stable
as $$
    select
        wi.id_work,

        count(*) filter (
            where wi.is_complit = false
        )::integer as workitemsnotcomplit,

        count(*) filter (
            where wi.is_complit = true
        )::integer as workitemscomplit

    from workitem wi
    where wi.id_work = any(p_work_ids)
      and wi.id_analiz is not null
      and not exists (
          select 1
          from analiz a
          where a.id_analiz = wi.id_analiz
            and a.is_group = true
      )
    group by wi.id_work;
$$;

-- ------------------------------------------------------------
-- Исходная функция SQL Server:
--
-- F_WORKS_LIST() была многооператорной табличной функцией:
-- она вставляла строки во внутреннюю табличную переменную и вызывала
-- F_WORKITEMS_COUNT_BY_ID_WORK два раза для каждой строки Works.
--
-- Оптимизированная версия для PostgreSQL:
-- 1. сначала берем только последние 3000 заказов;
-- 2. считаем элементы заказов одним вызовом функции;
-- 3. присоединяем счетчики обратно к заказам.
--
-- Клиентский запрос:
--     select *
--     from f_works_list()
--     limit 3000;
--
-- Функция сама ограничивает результат 3000 строками, потому что исходный
-- клиентский запрос не всегда может надежно протолкнуть LIMIT внутрь процедурной функции.
-- ------------------------------------------------------------

create or replace function f_works_list()
returns table
(
    id_work integer,
    create_date timestamp,
    materialnumber numeric(8, 2),
    is_complit boolean,
    fio varchar(255),
    d_date varchar(10),
    workitemsnotcomplit integer,
    workitemscomplit integer,
    full_name varchar(101),
    statusid smallint,
    statusname varchar(255),
    is_print boolean
)
language sql
stable
as $$
    with top_works as (
        select
            w.id_work,
            w.create_date,
            w.materialnumber,
            w.is_complit,
            w.fio,
            w.id_employee,
            w.statusid,
            w.print_date,
            w.sendtoclientdate,
            w.sendtodoctordate,
            w.sendtoorgdate,
            w.sendtofax
        from works w
        where w.is_del <> true
        order by w.id_work desc
        limit 3000
    ),
    work_ids as (
        select array_agg(tw.id_work) as ids
        from top_works tw
    ),
    workitem_counts as (
        select c.*
        from work_ids ids
        cross join f_workitems_count_by_id_work(ids.ids) c
    )
    select
        tw.id_work,
        tw.create_date,
        tw.materialnumber,
        tw.is_complit,
        tw.fio,

        to_char(tw.create_date, 'DD.MM.YYYY')::varchar(10) as d_date,

        coalesce(wic.workitemsnotcomplit, 0) as workitemsnotcomplit,
        coalesce(wic.workitemscomplit, 0) as workitemscomplit,

        f_employee_fullname(tw.id_employee) as full_name,

        tw.statusid,
        ws.statusname,

        (
            tw.print_date is not null
            or tw.sendtoclientdate is not null
            or tw.sendtodoctordate is not null
            or tw.sendtoorgdate is not null
            or tw.sendtofax is not null
        ) as is_print

    from top_works tw
    left join workitem_counts wic
        on wic.id_work = tw.id_work
    left join workstatus ws
        on ws.statusid = tw.statusid
    order by tw.id_work desc;
$$;

